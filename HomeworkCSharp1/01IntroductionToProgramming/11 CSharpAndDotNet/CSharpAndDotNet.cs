// Describe the difference between C# and .NET Framework

using System;

class CSharpAndDotNet
{
    static void Main()
    {
        Console.WriteLine("Езикът C# не се разпространява самостоятелно, а е част от платформата \nMicrosoft .NET Framework.");
        Console.WriteLine(".NET Framework представлява среда за разработка и изпълнение на програми, \nнаписани на езика C# или друг език, съвместим с .NET (като VB.NET, \nManaged C++, J# или F#).");
    }
}

